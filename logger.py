# =====================================================================
# فایل: logger.py
# توضیح: ماژول ثبت رویداد (Logging)
#         وظیفه‌ی ثبت و نمایش لاگ عملیات را بر عهده دارد.
#         این کلاس می‌تواند در آینده به Singleton تبدیل شود.
# =====================================================================


class TicketLogger:
    """
    کلاس ثبت‌کننده‌ی رویدادهای تیکت.
    اطلاعات پردازش هر تیکت را ثبت می‌کند.
    """

    def log(self, ticket) -> None:
        """
        اطلاعات نهایی تیکت را لاگ می‌کند.
        این متد در پایان پردازش هر تیکت فراخوانی می‌شود.
        """
        print(
            f"\n📋 [LOG] ثبت رویداد: "
            f"تیکت #{ticket.ticket_id} "
            f"→ وضعیت: {ticket.get_status_name()}"
        )
